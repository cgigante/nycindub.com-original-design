                  <article class="main-article"><p>In 2007, after finding an out-of-print album by Z-Factor, a house-music pioneer, on eBay, she promptly tracked down the group’s frontman in Chicago, remastered the original vinyl pressing and reissued 1,000 copies — which she did while living in her mother’s apartment on the Upper East Side.</p>
                  <p>"My mom helped me insert the band posters," Ms. Vasicka, 36, said the other day. She was sitting in her home studio in Greenpoint, Brooklyn, in a vintage wool sweater, Oak jeans and lavender cashmere socks, surrounded by analog synthesizers and boxes of '80s techno tapes.</p>
                  <p>Since founding Minimal Wave, a seven-year-old label dedicated to re-releasing obscure electronic music from the 1970s and ’80s, Ms. Vasicka has become an unwitting ambassador for the current synth revival, typified by so-called chillwave acts like Ariel Pink and Neon Indian.</p>
                  <p>And with her show on East Village Radio, an online station she helped found, and her D.J. gigs in New York, Berlin and Paris, she has resuscitated the careers of more than a dozen forgotten groups, including Oppenheimer Analysis and Das Ding, who are now touring and working on new music after a 20-year hiatus.</p>
                  <p>Moreover, Ms. Vasicka, a willowy brunette whose warm disposition is at odds with the frosty computer-generated music she puts out, has accomplished such feats almost entirely on her own, running her label with the D.I.Y. sensibility of the musicians she promotes.</p>
                  <p>"You can’t get a feel for what the artist intended by downloading an MP3 off the Web," she said, picking up a lovingly designed cardboard cassette case by Die Werkpiloten, a German new-wave band. “Music was supposed to be delivered in this handmade way.</p>
                  <p>This year marks a new stage in Ms. Vasicka’s career. In January, she opened a storefront in Greenpoint to serve as the retail arm and headquarters of Minimal Wave. In February, Stones Throw Records, a label founded by Chris Manak (otherwise known as Peanut Butter Wolf, a hip-hop D.J.), released the highly anticipated follow-up to "Minimal Wave Tapes: Volume One," a best-of compilation that the music Web site Pitchfork hailed as the Best Reissue of 2010...</p>
                  <p class="p-to-cursor" id="continue-reading"><strong>CONTINUE READING</strong></p>
                    <p style="display: none">Her growing fame is well deserved, Mr. Manak said.</p>
                    <p style="display: none">"She's been holding the torch for weird synth music since she was, like, 10," he said.</p>
                    <p style="display: none">Ms. Vasicka was raised on the Upper East Side, but found herself drawn to the downtown club scene. At 14 she was going to the Limelight and the Pyramid Club to see European gothic industrial bands like Front 242 and Einstürzende Neubauten.</p>
                    <p style="display: none">"They were so extreme," she said in her soft, almost whispery voice. "They'd pour buckets of scrap metal onto microphone pickups onstage. It made the loudest noises!"</p>
                    <p style="display: none">She attended the preppy Dalton School in Manhattan, where her goth aesthetic and multicolored hair drew stares. She later enrolled at the Rhode Island School of Design to study photography, but her music fascination endured.</p>
                    <p style="display: none">"I'd screen VHS tapes of rare Throbbing Gristle shows in my dorm on Friday nights," she said, referring to the late '70s British avant-garde industrial band.</p>
                    <p style="display: none">Ms. Vasicka returned to New York City after college, dabbling in graphic design and photographing artists like Cat Power, Jon Spencer and Cindy Sherman for fashion and music magazines. But she struggled to build a freelance photography career.</p>
                    <p style="display: none">On a whim in 2003, she took a job as an assistant to Frank Prisinzano, the East Village restaurateur behind Frank and Lil' Frankies, who had an interest in community radio. When not sewing curtains for Lil' Frankies, she helped him start East Village Radio — originally a pirate station — in the pizzeria's cluttered second-floor office. (The station now occupies a storefront on First Avenue.)</p>
                    <p style="display: none">The Minimal Wave label sprang from her weekly radio show of the same name. (She coined the phrase to the describe the bare-bones sound.) She tested her more-arcane tracks, which she'd find at record fairs, during D.J. sets in Williamsburg and the Lower East Side.</p>
                    <p style="display: none">"People would ask, ‘Where can I buy this?' And I'd say: ‘You can't. But maybe I should do something about that.' "</p>
                    <p style="display: none">In response, she put out Minimal Wave's first release in 2005, an EP by Oppenheimer Analysis, a little-known band. The 500 copies quickly sold out, and in 2007 Uniqlo licensed a quote from the track "Devil's Dancers" for an ad campaign. Since then, the lead singer, Andy Oppenheimer, who is now 57 and works in London as an independent consultant on nuclear weapons and counterterrorism, has performed throughout Europe.</p>
                    <p style="display: none">Thirty-five releases later, Minimal Wave is a self-sustaining business.</p>
                    <p style="display: none">"Synth has a huge following," Ms. Vasicka said, opening a spreadsheet of recent orders from Australia, Sweden, Estonia and Singapore.</p>
                    <p style="display: none">She has even hired a part-time employee, an indie rock musician named Jake Pepper. On a recent afternoon, Mr. Pepper, wearing a purple hoodie, was huddled beside a space heater in Minimal Wave's spare storefront, sleeving copies of a hand-silk-screened EP by the Dutch group Schedelvreter, whose name roughly translates as "skull gobbler."</p>
                    <p style="display: none">Meanwhile, Ms. Vasicka sampled a few tracks from "Minimal Wave Tapes: Volume Two," including the extremely lo-fi "Animals from Outer Space" by a Dutch act called Ende Shneafliet.</p>
                    <p style="display: none">"These guys were syncing the drum and synth tracks by hand, so you can hear the human error," she said approvingly. "It reminds you that there's a person behind the machines."</p> 
                

